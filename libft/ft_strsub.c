#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	i = 0;
	if (s)
		return (NULL);
	if (len > ft_strlen(s))
		return (0);
	if ((str = (char *)malloc(sizeof(*str) * (len + 1))) == 0)
		return (0);
	while (i < len)
	{
		str[i] = s[start];
		i++;
		start++;
	}
	str[i] = '\0';
	return (str);
}
